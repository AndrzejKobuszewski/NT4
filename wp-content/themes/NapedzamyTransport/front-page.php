          <td colspan = "6"><br><h3><b>Tło projektu <i>"Napędzamy Transport 4.0"</i></b></h3>
          <br>
          Pomysł na projekt zrodził się podczas pracy w administracji publicznej. Zaobserwowano tam kilka prawidłowości,
            które w przekonaniu pomysłodawcy projektu stanowią istotną przeszkodę w prawidłowym rozwoju systemu transportowego kraju:<br>
          <ol>
            <li> Bardzo niskie zaangażowanie społeczne w sprawy transportu, przejawiające się znikomą liczbą skarg i wniosków
               wpływających do urzędów oraz niskim poziomem świadomości społecznej o fundamentalnych zasadach działania systemu
                transportowego w kraju. Bez aktywnej społeczności wokół spraw związnych z mobilnością ludzi i towarów nie jest
                 możliwy zrównoważony rozwój systemu transporotowego w zgodzie z oczekiwaniami aktorów tego systemu.<br><br></li>
            <li> Jako wieloletni pracownik administracji publicznej stwierdzam, że dużo czasu poświęca się wyszukiwaniu
              podstawowych informacji. W przekonaniu pomysłodawcy serwisu czas ten można spożytkować na inne działania.
              Dodatkowo należy stwierdzić, że w ślad za nakłądanymi dodatkowymi zadanimi na urzędników nie idą zasoby,
              które mają oni do dyspozycji.<br><br></li>
            <li> Niepewność decyzji administracji publicznej bardzo źle wpływa na inwestycje w przedsiębiorstwach;
              ciągłe zmiany na stanowiskach kierowniczych i brak konsekwentnie realizowanych strategii u największych
              zamawiających uniemożliwiają długoterminowe planowanie rozwoju firm komercyjnych.<br><br></li>
            <li> Brak powyższych strategii inwestycyjnych i rozwoju uniemożliwia również wsparcie rozowju mobilności przez
               intytucje naukowe, które wkładają wysiłki w tworzenie rozwiązań niedostosowanych do realiów procedur
               administracyjnych, lub legislacji.<br><br></li>
            <li> Wszystkie powyższe problemy kumulują się w podejściu do wydatkowania pieniędzy, które nie jest oparte na pomiary,
               lecz zgłoszenia potrzeb przez zarządzających. Brak konsekwetnie realizowanych inwestycji oraz rzetelnego planowania
                nakładów na utrzymanie, jak również brak aktywności społecznej w sprawach mobilności skutkuje ograniczeniem
                perspektywy zarządczej do pojedynczych, najczęściej infrastrukturalnych projektów, które zamiast prawdziwych
                mierników rezultatu mają wskaźniki w postacji bezwzględnej liczby km nowych dróg.<br>
            </li>
          </ol>
